#!/usr/bin/env node

/**
 * Generate a compact local U.S. places index from the official U.S. Census
 * Gazetteer national places file.
 *
 * Source file example:
 *   /tmp/census_places/2024_Gaz_place_national.txt
 *
 * Output:
 *   lib/generated/usCityIndex.generated.ts
 */

import fs from 'node:fs';
import path from 'node:path';

const INPUT_PATH = process.argv[2] || '/tmp/census_places/2024_Gaz_place_national.txt';
const OUTPUT_PATH = path.resolve(process.cwd(), 'lib/generated/usCityIndex.generated.ts');

const ALLOWED_STATES = new Set([
  'AL', 'AK', 'AZ', 'AR', 'CA', 'CO', 'CT', 'DE', 'FL', 'GA',
  'HI', 'ID', 'IL', 'IN', 'IA', 'KS', 'KY', 'LA', 'ME', 'MD',
  'MA', 'MI', 'MN', 'MS', 'MO', 'MT', 'NE', 'NV', 'NH', 'NJ',
  'NM', 'NY', 'NC', 'ND', 'OH', 'OK', 'OR', 'PA', 'RI', 'SC',
  'SD', 'TN', 'TX', 'UT', 'VT', 'VA', 'WA', 'WV', 'WI', 'WY', 'DC',
]);

function stripPlaceSuffix(name) {
  return name
    .replace(/\s+\(balance\)$/i, '')
    .replace(/\s+(city|town|village|borough|municipio|municipality|CDP)$/i, '')
    .trim();
}

function rankPlaceType(name) {
  if (/\s+CDP$/i.test(name)) return 1;
  return 0;
}

const raw = fs.readFileSync(INPUT_PATH, 'utf8').trim().split(/\r?\n/);
const rows = raw.slice(1);
const seen = new Set();
const output = [];

for (const line of rows) {
  const cols = line.split('\t');
  if (cols.length < 11) continue;
  const stateCode = cols[0]?.trim();
  const rawName = cols[3]?.trim();
  const lat = Number(cols[9]);
  const lon = Number(cols[10]);
  if (!stateCode || !ALLOWED_STATES.has(stateCode) || !rawName || !Number.isFinite(lat) || !Number.isFinite(lon)) {
    continue;
  }

  const cityName = stripPlaceSuffix(rawName);
  if (!cityName) continue;

  const key = `${cityName}|${stateCode}|${lat.toFixed(4)}|${lon.toFixed(4)}`;
  if (seen.has(key)) continue;
  seen.add(key);

  output.push([cityName, stateCode, Number(lat.toFixed(6)), Number(lon.toFixed(6)), rankPlaceType(rawName)]);
}

output.sort((a, b) => {
  if (a[0] !== b[0]) return a[0].localeCompare(b[0]);
  if (a[1] !== b[1]) return a[1].localeCompare(b[1]);
  return a[4] - b[4];
});

const header = `/* eslint-disable */\n// Generated by scripts/generate-us-city-index.mjs from the official U.S. Census Gazetteer places file.\n\nexport type USCityIndexRow = readonly [name: string, stateCode: string, lat: number, lon: number, placeRank: number];\n\nexport const US_CITY_INDEX: readonly USCityIndexRow[] = `;
const body = JSON.stringify(output);
const footer = ' as const;\n';

fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
fs.writeFileSync(OUTPUT_PATH, header + body + footer);

console.log(`Wrote ${output.length} places to ${OUTPUT_PATH}`);
